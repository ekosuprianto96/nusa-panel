// ============================================
// TYPES
// ============================================

export type ColorScale = keyof typeof primitiveColors
export type ColorShade = 50 | 100 | 200 | 300 | 400 | 500 | 600 | 700 | 800 | 900 | 950

/**
 * Get a primitive color value
 */
export function getPrimitiveColor(
    scale: ColorScale,
    shade?: ColorShade
): string {
    const color = primitiveColors[scale]
    if (typeof color === 'string') return color
    return shade ? color[shade] : color[500]
}

export default primitiveColors
